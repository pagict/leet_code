int CascadeTotal(const vector<vector<int>> &triangle,
                 vector<vector<int>> &cache, int i, int j) {
  auto &cache_val = cache[i][j];
  if (cache_val != -1) {
    return cache_val;
  }
  auto num = triangle[i][j];
  if (i == triangle.size() - 1) {
    return num;
  }
  auto a = CascadeTotal(triangle, cache, i + 1, j) + num;
  auto b = CascadeTotal(triangle, cache, i + 1, j + 1) + num;
  cache_val = std::min(a, b);
  return cache_val;
}

int minimumTotal(vector<vector<int>> &triangle) {
  auto cache = triangle;
  for (auto &row : cache) {
    for (auto i = 0; i < row.size(); ++i) {
      row[i] = -1;
    }
  }
  return CascadeTotal(triangle, cache, 0, 0);
}

